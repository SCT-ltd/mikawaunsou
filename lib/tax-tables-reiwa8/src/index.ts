/**
 * 令和8年分 給与所得の源泉徴収税額（月額表 甲欄）を返す。
 *
 * - 0〜739,999円: 公式表の完全ハードコード値（1円単位完全一致）
 * - 740,000円以上: 公式表の計算式（20.42%〜45.945%）
 *
 * @param afterInsuranceSalary 社会保険料等控除後の給与等の金額（非課税手当除く）
 * @param dependentEquivCount  扶養親族等の数（配偶者含む合計、0〜7）
 * @returns 源泉徴収税額（円）
 *
 * @example
 * calculateIncomeTaxReiwa8MonthlyKou(652_526, 2) // → 41_840（三川兼司）
 * calculateIncomeTaxReiwa8MonthlyKou(371_000, 1) // → 10_220（玉川裕樹）
 * calculateIncomeTaxReiwa8MonthlyKou(338_000, 0) // → 10_750
 */
export function calculateIncomeTaxReiwa8MonthlyKou(
  afterInsuranceSalary: number,
  dependentEquivCount: number,
): number {
  const salary = Math.max(0, Math.floor(afterInsuranceSalary));
  const dep    = Math.min(7, Math.max(0, Math.floor(dependentEquivCount)));

  // 0〜739,999円: 公式表ハードコード参照
  if (salary < 740_000) {
    const row = _lookupOfficial(salary);
    return row[dep + 1];
  }

  // 740,000円以上: 計算式区分
  for (const seg of FORMULA_SEGMENTS) {
    if (salary >= seg.lower && salary < seg.upper) {
      const base   = seg.base[dep];
      const excess = salary - seg.lower;
      return Math.floor(base + excess * seg.rate);
    }
  }

  return 0;
}
