function _lookupOfficial(salary: number): readonly [number,number,number,number,number,number,number,number,number] {
  let lo = 0;
  let hi = OFFICIAL_ROWS.length - 1;
  while (lo < hi) {
    const mid = (lo + hi + 1) >> 1;
    if (OFFICIAL_ROWS[mid][0] <= salary) lo = mid;
    else hi = mid - 1;
  }
  return OFFICIAL_ROWS[lo];
}

