// ────────────────────────────────────────────────────────────────────────────

/**
 * 令和8年（2026年）源泉徴収税額を計算する（月額表甲欄）
 * 基礎控除額が480,000→580,000に増加した改正後。
 * 令和8年の公式テーブル公示後にテーブル方式へ移行予定。
 *
 * @param afterInsuranceSalary 社会保険料等控除後の給与等の金額
 * @param dependentEquivCount  扶養親族等の数（配偶者を含む合計、0〜7）
 */
export function calculateIncomeTaxReiwa8(
  afterInsuranceSalary: number,
  dependentEquivCount: number,
): number {
  if (afterInsuranceSalary < 88000) return 0;
  let row = INCOME_TAX_TABLE_REIWA8.find((r: any) => afterInsuranceSalary >= r.min && afterInsuranceSalary < r.max);
  if (!row) {
    if (afterInsuranceSalary >= INCOME_TAX_TABLE_REIWA8[INCOME_TAX_TABLE_REIWA8.length - 1].max) {
       row = INCOME_TAX_TABLE_REIWA8[INCOME_TAX_TABLE_REIWA8.length - 1];
    } else {
       return 0;
    }
  }
  const depIndex = Math.min(Math.max(Math.floor(dependentEquivCount), 0), 3);
  return row.dependents[depIndex] ?? 0;
}

/**
 * 年度を指定して源泉徴収税額を計算する
 *
 * @param afterInsuranceSalary 社会保険料等控除後の給与等の金額
 * @param dependentEquivCount  扶養親族等の数（配偶者を含む合計、0〜7）
 * @param reiwaYear            令和の年号（7=令和7年、8=令和8年）
 */
export function calculateIncomeTax(
  afterInsuranceSalary: number,
  dependentEquivCount: number,
  reiwaYear: 7 | 8 = 7,
): number {
  return reiwaYear >= 8
    ? calculateIncomeTaxReiwa8(afterInsuranceSalary, dependentEquivCount)
    : calculateIncomeTaxReiwa7(afterInsuranceSalary, dependentEquivCount);
}



