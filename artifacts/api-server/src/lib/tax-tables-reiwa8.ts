/**
 * 令和8年 源泉徴収税額（月額表甲欄）を計算する
 *
 * @param afterInsuranceSalary 社会保険料等控除後の給与等の金額
 * @param dependentEquivCount  扶養親族等の数（配偶者を含む合計、0-7）
 */
export function calculateIncomeTaxReiwa8(
  afterInsuranceSalary: number,
  dependentEquivCount: number,
): number {
  const depIdx = Math.min(7, Math.max(0, Math.floor(dependentEquivCount)));
  const salary = Math.floor(afterInsuranceSalary);

  // 該当する行を検索（以上の最大値を採用）
  let row = MONTHLY_TAX_TABLE[0];
  for (const entry of MONTHLY_TAX_TABLE) {
    if (salary >= entry[0]) {
      row = entry;
    } else {
      break;
    }
  }

  return row[depIdx + 1] as number;
}
